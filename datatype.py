# 資料:程式的基本單位
# 數字
3456
# 浮點數(float)
3.6
# 字串(str)
"你好啊"
"hello"
# 布林值
True
False
# 有順序,可變動的列表 List
[3,4,5]
["hello","world"]
# 有順序,不可變動的列表 Tuple
(3,4,5)
("hello","world")
# 集合 Set,沒有順序區分
{3,4,5}
# 字典 Dictionary
{"apple":"蘋果","data":"資料"}
# 變數:用來儲存資料的自訂名稱
# 變數名稱=資料
x=3
# print(資料)
print(x)
x=True # 取代舊的資料
print(x)
x="Hello"
print(x)
x={3,4,5}
print(x)